def ungkapkan_perasaan(nama):
    print("Halo", nama + "!")
    print("Saya ingin berbicara tentang perasaan saya.")
    print("Saya merasa ... (ungkapkan perasaan Anda di sini)")
    print("Itu adalah perasaan yang penting bagi saya, dan saya ingin Anda tahu tentang itu.")
    print("Terima kasih telah mendengarkan. Apakah Anda memiliki sesuatu yang ingin Anda bagikan juga?")

# Contoh penggunaan:
nama_penerima = input("Masukkan nama penerima: ")
ungkapkan_perasaan(nama_penerima)
